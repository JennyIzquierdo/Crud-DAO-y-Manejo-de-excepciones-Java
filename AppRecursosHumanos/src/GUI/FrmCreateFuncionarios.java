    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
        // Check all fields
        
        //Recopile records
        
        String nombres = jTextNombres.getText();
        String apellidos = jTextApellidos.getText();
        String direccion = jTextDireccion.getText();
        String numeroId = jTextNumeroId.getText();
        String tipoId = cmbTipoId.getSelectedItem().toString();
        String estadoCivil = cmbEstadoCivil.getSelectedItem().toString();
        String sexo = cmbSexo.getSelectedItem().toString();
        String tipoVia = cmbTipoVia.getSelectedItem().toString();        
        String fecha_nac = jFormattedFechaNac.getText();
        String telefono = jTextTelefono.getText();
        
        
        // validate empty fields
        
        if (!"".equals(nombres) && !"".equals(apellidos) && !"".equals(direccion) 
                && !"".equals(numeroId) && !"Seleccionar una opcion".equals(tipoId) 
                && !"Seleccionar una opcion".equals(estadoCivil) && !"Seleccionar una opcion".equals(sexo) 
                && !"Seleccionar una opcion".equals(tipoVia) && !"".equals(fecha_nac) && !"".equals(telefono)) {
            
            // Create Funcionario Object and mapping the info
            Funcionario _funcionario = new Funcionario();
            
            _funcionario.setNombres(nombres);
            _funcionario.setApellidos(apellidos);
            _funcionario.setDireccion(tipoVia + " " +direccion);
            _funcionario.setEstadoCivil(estadoCivil);
            
            try {
                Date _fechaNac = new SimpleDateFormat("yyyy-MM-dd").parse(fecha_nac);
                _funcionario.setFechaNacimiento(_fechaNac);
            } catch (ParseException ex) {
                Logger.getLogger(FrmCreateFuncionarios.class.getName()).log(Level.SEVERE, null, ex);
            }
            _funcionario.setSexo(sexo);
            _funcionario.setNumeroIdentificacion(numeroId);
            _funcionario.setTipoIdentificacion(tipoId);
            _funcionario.setTelefono(telefono);
            
            // Send Object
            
            try {
                FuncionarioDao funcionariosDao = new FuncionarioImpl();
                funcionariosDao.createFuncionario(_funcionario);
                
                //ok -  user
                String _message = "Usuario creado correctament";
                String _title = "Information";
                JOptionPane.showMessageDialog(null, _message, _title, JOptionPane.INFORMATION_MESSAGE); 
                clearComponents();
            } catch (HeadlessException e) {
                System.out.println(e.getMessage());
            }
        } else
        {
            String _message = "Campos vacios o sin seleccionar, por favor verifique";
            String _title = "Warning";
            JOptionPane.showMessageDialog(null, _message, _title, JOptionPane.WARNING_MESSAGE); 
        }
    }//GEN-LAST:event_jBtnGuardarActionPerformed

